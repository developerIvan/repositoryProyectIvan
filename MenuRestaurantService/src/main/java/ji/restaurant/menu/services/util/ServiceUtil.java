package ji.restaurant.menu.services.util;

import java.util.List;


/**
 * <b>REVISION 1.0</b>
 * <br>
 * <b>Fecha:</b>01/07/2015</b>
 * <b>Contiene funciones para los servicios</b>
 * @author Ivan
 * @version 1.0
 */
public class ServiceUtil {

	
	/**
	 * Valida si una lista es vacia o nula
	 * @param listParam
	 * @return
	 */
	public static boolean isEmptyOrNull(List<?> listParam){
		return null==listParam||listParam.isEmpty();
	}
	
	
	/**
	 * Evalua si un objeto es nulo 
	 * @param object
	 * @return
	 */
	public static boolean isNullObject(Object object){
		return null==object;
	}
	
}
